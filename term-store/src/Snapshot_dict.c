void init_Snapshot_dict()
{
  ATermList afuns, terms;

  _Snapshot_dict = ATreadFromBinaryString(_Snapshot_dict_baf, _Snapshot_dict_LEN);

  ATprotect(&_Snapshot_dict);

  afuns = (ATermList)ATelementAt((ATermList)_Snapshot_dict, 0);

  SS_afun0 = ATgetAFun((ATermAppl)ATgetFirst(afuns));
  afuns = ATgetNext(afuns);
  SS_afun2 = ATgetAFun((ATermAppl)ATgetFirst(afuns));
  afuns = ATgetNext(afuns);
  SS_afun1 = ATgetAFun((ATermAppl)ATgetFirst(afuns));
  afuns = ATgetNext(afuns);

  terms = (ATermList)ATelementAt((ATermList)_Snapshot_dict, 1);

  SS_patternRowDefault = ATgetFirst(terms);
  terms = ATgetNext(terms);
  SS_patternRowsMany = ATgetFirst(terms);
  terms = ATgetNext(terms);
  SS_patternRowsSingle = ATgetFirst(terms);
  terms = ATgetNext(terms);
  SS_patternRowsEmpty = ATgetFirst(terms);
  terms = ATgetNext(terms);
  SS_patternTableDefault = ATgetFirst(terms);
  terms = ATgetNext(terms);
  SS_patternTablesMany = ATgetFirst(terms);
  terms = ATgetNext(terms);
  SS_patternTablesSingle = ATgetFirst(terms);
  terms = ATgetNext(terms);
  SS_patternTablesEmpty = ATgetFirst(terms);
  terms = ATgetNext(terms);
  SS_patternSnapshotMain = ATgetFirst(terms);
  terms = ATgetNext(terms);
}
