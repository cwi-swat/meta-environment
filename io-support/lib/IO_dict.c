#include "IO_dict.h"

AFun IO_afun2;
AFun IO_afun3;
AFun IO_afun0;
AFun IO_afun1;


/*
 * IO_afun2 = relative(x)
 * IO_afun3 = segment(x)
 * IO_afun0 = file(x,x,x)
 * IO_afun1 = absolute(x)
 *
 *
 */

static ATerm _IO_dict = NULL;

#define _IO_dict_LEN 99

static unsigned char _IO_dict_baf[_IO_dict_LEN] = {
0x00,0x8B,0xAF,0x83,0x00,0x07,0x0C,0x05,0x5B,0x5F,0x2C,0x5F,0x5D,0x02,0x00,0x06,
0x06,0x00,0x01,0x03,0x04,0x05,0x06,0x02,0x00,0x01,0x02,0x5B,0x5D,0x00,0x00,0x01,
0x01,0x78,0x00,0x00,0x01,0x08,0x72,0x65,0x6C,0x61,0x74,0x69,0x76,0x65,0x01,0x00,
0x01,0x01,0x02,0x07,0x73,0x65,0x67,0x6D,0x65,0x6E,0x74,0x01,0x00,0x01,0x01,0x02,
0x04,0x66,0x69,0x6C,0x65,0x03,0x00,0x01,0x01,0x02,0x01,0x02,0x01,0x02,0x08,0x61,
0x62,0x73,0x6F,0x6C,0x75,0x74,0x65,0x01,0x00,0x01,0x01,0x02,0x00,0x19,0x0B,0x10,
0x82,0xC1,0x90
};

void init_IO_dict()
{
  ATermList afuns, terms;

  _IO_dict = ATreadFromBinaryString(_IO_dict_baf, _IO_dict_LEN);

  ATprotect(&_IO_dict);

  afuns = (ATermList)ATelementAt((ATermList)_IO_dict, 0);

  IO_afun2 = ATgetAFun((ATermAppl)ATgetFirst(afuns));
  afuns = ATgetNext(afuns);
  IO_afun3 = ATgetAFun((ATermAppl)ATgetFirst(afuns));
  afuns = ATgetNext(afuns);
  IO_afun0 = ATgetAFun((ATermAppl)ATgetFirst(afuns));
  afuns = ATgetNext(afuns);
  IO_afun1 = ATgetAFun((ATermAppl)ATgetFirst(afuns));
  afuns = ATgetNext(afuns);

  terms = (ATermList)ATelementAt((ATermList)_IO_dict, 1);

}
